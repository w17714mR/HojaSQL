-- 1.2
SELECT
   TO_CHAR(RUT, '999G999G999') || '-' || DV AS RUN,
   NOMBRES || ' ' || APELLIDOS AS NOMBRE,
   PUNTAJE,
   TO_CHAR(SUELDO, '$999G999G999') AS SUELDO
FROM
   EMPLEADO
ORDER BY
   APELLIDOS;

-- 1.3
SELECT
   ZONA,
   PUNTAJE,
   NOMBRES || ' ' || APELLIDOS AS NOMBRE,
   TO_CHAR(SUELDO, '$999G999G999') AS SUELDO
FROM
   EMPLEADO
ORDER BY
   1;

