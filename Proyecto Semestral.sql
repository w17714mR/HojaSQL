SELECT
   UPPER(EDI_NOMBRE_EDIFICIO) AS "NOMBRE EDIFICIO",
   COUNT(*) AS "TOTAL DEPTOS",
   NVL (C1.CANTIDAD,
      0) AS "TOTAL DEPTOS 1 DORMITORIO",
   NVL (C2.CANTIDAD,
      0) AS "TOTAL DEPTOS 2 DORMITORIOS",
   NVL (C3.CANTIDAD,
      0) AS "TOTAL DEPTOS 3 DORMITORIOS",
   NVL (C4.CANTIDAD,
      0) AS "TOTAL DEPTOS 4 DORMITORIOS",
   NVL (C5.CANTIDAD,
      0) AS "TOTAL DEPTOS 5 DORMITORIOS"
FROM
   EDIFICIO E
   JOIN DEPARTAMENTO D USING (ID_EDIFICIO)
   LEFT JOIN (
      SELECT
         ID_EDIFICIO,
         COUNT(*) AS CANTIDAD
      FROM
         DEPARTAMENTO D
         JOIN EDIFICIO USING (ID_EDIFICIO)
      WHERE
         TOTAL_DORMITORIOS = 1
      GROUP BY
         ID_EDIFICIO) C1 USING (ID_EDIFICIO)
      LEFT JOIN (
         SELECT
            ID_EDIFICIO,
            COUNT(*) AS CANTIDAD
         FROM
            DEPARTAMENTO D
            JOIN EDIFICIO USING (ID_EDIFICIO)
         WHERE
            TOTAL_DORMITORIOS = 2
         GROUP BY
            ID_EDIFICIO) C2 USING (ID_EDIFICIO)
         LEFT JOIN (
            SELECT
               ID_EDIFICIO,
               COUNT(*) AS CANTIDAD
            FROM
               DEPARTAMENTO D
               JOIN EDIFICIO USING (ID_EDIFICIO)
            WHERE
               TOTAL_DORMITORIOS = 3
            GROUP BY
               ID_EDIFICIO) C3 USING (ID_EDIFICIO)
            LEFT JOIN (
               SELECT
                  ID_EDIFICIO,
                  COUNT(*) AS CANTIDAD
               FROM
                  DEPARTAMENTO D
                  JOIN EDIFICIO USING (ID_EDIFICIO)
               WHERE
                  TOTAL_DORMITORIOS = 4
               GROUP BY
                  ID_EDIFICIO) C4 USING (ID_EDIFICIO)
               LEFT JOIN (
                  SELECT
                     ID_EDIFICIO,
                     COUNT(*) AS CANTIDAD
                  FROM
                     DEPARTAMENTO D
                     JOIN EDIFICIO USING (ID_EDIFICIO)
                  WHERE
                     TOTAL_DORMITORIOS = 5
                  GROUP BY
                     ID_EDIFICIO) C5 USING (ID_EDIFICIO)
               GROUP BY
                  EDI_NOMBRE_EDIFICIO, C1.CANTIDAD, C2.CANTIDAD, C3.CANTIDAD, C4.CANTIDAD, C5.CANTIDAD
               ORDER BY
                  1;

