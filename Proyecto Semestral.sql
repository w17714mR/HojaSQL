   ID_EDIFICIO,
   EDIFICIO.EDI_NOMBRE_EDIFICIO,
   NOMBRE_COMUNA,
   GAS_FECHA_DESDE,
   GAS_FECHA_HASTA
ORDER BY
   3 ASC,
   1 ASC;

